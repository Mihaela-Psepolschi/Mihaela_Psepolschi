    private void showDepartments(){
        int numberEmployees = Integer.parseInt(jTextField2.getText());
        try {
            List<Department> departments = ClientController.getInstance().getDepartments(numberEmployees);
            model.clear();
            departments.forEach(model::addElement);
        } catch (RemoteException ex) {
            JOptionPane.showMessageDialog(null, "We have a problem with server connection.");
        }
    }

