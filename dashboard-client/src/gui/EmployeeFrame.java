    private void addEmployee(){
        String employeeName = jTextField1.getText();
        Employee employee = new Employee();
        employee.setName(employeeName);
        try {
            ClientController.getInstance().addEmployee(employee);
            JOptionPane.showMessageDialog(null, "An employee was added !");            
        } catch (RemoteException ex) {
            JOptionPane.showMessageDialog(null, "We have a problem with server connection.");
        }
    }

